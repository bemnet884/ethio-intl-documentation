// Build reverse map for O(1) lookups
export const reverseMap: Record<string, string> = {};
Object.keys(amharicMap).forEach(key => {
  const data = amharicMap[key];
  if (data.base) reverseMap[data.base] = key;
  ['ä', 'a', 'u', 'i', 'e', 'o'].forEach(vowel => {
    if (data[vowel]) {
      reverseMap[data[vowel] as string] = key;
    }
  });
});
